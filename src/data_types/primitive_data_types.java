package data_types;

public class primitive_data_types {

	public static void main(String[] args) {
		//integer data type
		byte age=25;
		short marks=57;
		System.out.println(age);
		System.out.println(marks);
		int salary =32000;
		long turnover = 599999;
		System.out.println(salary);
		System.out.println(turnover); 
		//boolean data type
		boolean a=true;
		//float pointing
		System.out.println(a);
		float sal=27000.50f;
		double turnnover=57983489.57;
		System.out.println(sal);
		System.out.println(turnnover);
		//char data type
		char Sname='p';
		System.out.println(Sname);
		
		
		
	}

}
